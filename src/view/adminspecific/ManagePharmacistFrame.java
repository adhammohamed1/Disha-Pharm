    private void changePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordButtonActionPerformed
        if (!passwordField.isEditable()) {
            changePasswordButton.setText("Apply");
            passwordField.grabFocus();
        } else {
            String input = passwordField.getText();
            if (!input.equals(tempPassword)) {
                String confirm = input.equals(displayedPharmacist.getPassword()) ? input : CustomOptionPane.showPasswordDialog();
                if (confirm != null && confirm.equals(input)) {
                    String process
                            = input.equals(displayedPharmacist.getPassword()) ?
                            "reset" : "changed successfully\nDon't forget to save your changes!";
                    CustomOptionPane.showMessageDialog("Pharmacist password has been " + process, "Success");
                    tempPassword = input;
                    saveChangesButton.setEnabled(!tempPassword.equals(displayedPharmacist.getPassword()));
                } else {
                    CustomOptionPane.showMessageDialog("Process Canceled");
                    passwordField.setText(tempPassword);
                }
            }
            changePasswordButton.setText("Change Password");
        }
        passwordField.setEditable(!passwordField.isEditable());
        passwordCancelButton.setVisible(passwordField.isEditable());
        adjustPasswordFieldView();
    }//GEN-LAST:event_changePasswordButtonActionPerformed

