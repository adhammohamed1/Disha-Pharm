    private void salaryConfirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salaryConfirmButtonActionPerformed
        double newSalary = Double.parseDouble(salaryField.getText());
        if (newSalary != tempSalary) {
            String word = newSalary > tempSalary ? "Raise" : "Cut";
            int choice = JOptionPane.showConfirmDialog(null,
                    word + " " + displayedPharmacist.getName() + "'s salary?" +
                            "\n\n   " + String.format("%.2f", tempSalary) + " ==> "
                            + String.format("%.2f", newSalary), "", JOptionPane.YES_NO_OPTION);

            if (choice == 0) {
                salaryField.setText(String.format("%.2f", newSalary));
                saveChangesButton.setEnabled(!(
                        String.format("%.2f", newSalary).equals(String.format("%.2f", displayedPharmacist.getSalary()))));
                tempSalary = newSalary;
            } else salaryField.setText(String.format("%.2f", tempSalary));
        }
        salaryField.setEditable(false);
        salaryField.transferFocus();
        salaryCancelButton.setVisible(false);
        salaryConfirmButton.setVisible(false);
        editSalaryButton.setVisible(true);
    }//GEN-LAST:event_salaryConfirmButtonActionPerformed

