    private void currencyComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_currencyComboBoxActionPerformed
        switch (currencyComboBox.getSelectedIndex()) {
            case FinancialFactors.USD:
                FinancialFactors.PREFERRED_CURRENCY = FinancialFactors.US_DOLLAR;
                break;
            case FinancialFactors.EGP:
                FinancialFactors.PREFERRED_CURRENCY = FinancialFactors.EGYPTIAN_POUND;
                break;
            case FinancialFactors.EUR:
                FinancialFactors.PREFERRED_CURRENCY = FinancialFactors.EURO;
                break;
            case FinancialFactors.STERLING:
                FinancialFactors.PREFERRED_CURRENCY = FinancialFactors.STERLING_POUND;
                break;
        }
    }//GEN-LAST:event_currencyComboBoxActionPerformed

