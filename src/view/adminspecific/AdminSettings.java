    void changePasswordFieldView() {
        if (passwordIsHidden) {
            jLabel9.setText("Show");
            jLabel9.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphics_and_icons/icon/tools/hide-icon.png")));

            String password = "";
            for (int i = 0; i < Admin.currentAdmin.getPassword().length(); i++) {
                password += "*";
            }
            passwordField.setText(password);

            passwordIsHidden = false;
        } else {
            jLabel9.setText("Hide");
            jLabel9.setIcon(new javax.swing.ImageIcon(getClass().getResource("/graphics_and_icons/icon/tools/eye-icon.png")));
            passwordField.setText(Admin.currentAdmin.getPassword());
            passwordIsHidden = true;
        }
    }

    private void doneButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneButtonActionPerformed
        int choice = 0;
        if (!changes.isEmpty()) {
            choice = JOptionPane.showConfirmDialog(
                    null, "You have unfinished changes. Exit anyway?", "", JOptionPane.YES_NO_OPTION);
        }
        if (choice == 0) {
            this.dispose();
            AdminHome adminHome = new AdminHome();
            adminHome.navigateTo(previousIndex);
            previousIndex = 0;
            adminHome.setVisible(true);
        }
    }//GEN-LAST:event_doneButtonActionPerformed

    private void resetLoginCredentialsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetLoginCredentialsButtonActionPerformed

        if (JOptionPane.showConfirmDialog(null, "Reset login credentials to default values?", "", JOptionPane.YES_NO_OPTION) == 0) {

            AdminAuthenticator.useDefault = true;
            AdminHandler.setDefaultLoginCredentials();

            new AdminFileHandler().saveToFile();

            usernameField.setText(Admin.currentAdmin.getUserName());
            passwordField.setText(Admin.currentAdmin.getPassword());
            changePasswordFieldView();

        }
    }//GEN-LAST:event_resetLoginCredentialsButtonActionPerformed

    private void changePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordButtonActionPerformed
        if (!changingPassword) {
            changePasswordButton.setText("Save");
            cancelButton4.setVisible(true);

            passwordField.setText(Admin.currentAdmin.getPassword());
            passwordField.setEditable(true);

            jLabel9.setVisible(false);

            changingPassword = true;
            changes.add(true);
        } else {
            String temp = passwordField.getText();

            if (!temp.equals(Admin.currentAdmin.getPassword())) {
                String confirm = CustomOptionPane.showPasswordDialog();

                if (confirm != null && confirm.equals(temp)) {
                    Admin.currentAdmin.setPassword(temp);
                    AdminAuthenticator.useDefault = Admin.currentAdmin.getUserName().equalsIgnoreCase(AdminAuthenticator.defaultAdminUsername) && Admin.currentAdmin.getPassword().equals(AdminAuthenticator.defaultAdminPassword);
                    new AdminFileHandler().saveToFile();
                    CustomOptionPane.showMessageDialog("Admin password has been changed successfully", "Success");
                }
            }

            jLabel9.setVisible(true);
            cancelButton4.setVisible(false);
            changePasswordButton.setText("Change Password");

            passwordField.setText(Admin.currentAdmin.getPassword());
            passwordField.setEditable(false);
            changePasswordFieldView();

            changingPassword = false;
            changes.remove(changes.size() - 1);
        }
    }//GEN-LAST:event_changePasswordButtonActionPerformed

    private void changeUsernameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changeUsernameButtonActionPerformed
        if (!changingUsername) {
            changeUsernameButton.setText("Save");
            cancelButton3.setVisible(true);

            usernameField.setEditable(true);

            changingUsername = true;
            changes.add(true);
        } else {
            String temp = usernameField.getText();

            if (!temp.equals(Admin.currentAdmin.getUserName())) {
                int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you wish to change the admin username?\n\n\"" + Admin.currentAdmin.getUserName() + "\" => \"" + temp + "\"", "Are you sure?", JOptionPane.YES_NO_OPTION);

                if (confirm == 0) {
                    Admin.currentAdmin.setUserName(temp);
                    AdminAuthenticator.useDefault = Admin.currentAdmin.getUserName().equalsIgnoreCase(AdminAuthenticator.defaultAdminUsername) && Admin.currentAdmin.getPassword().equals(AdminAuthenticator.defaultAdminPassword);
                    new AdminFileHandler().saveToFile();
                    CustomOptionPane.showMessageDialog("Admin username has been changed successfully", "Success");
                }
            }

            changeUsernameButton.setText("Change Username");

            cancelButton3.setVisible(false);

            usernameField.setText(Admin.currentAdmin.getUserName());
            usernameField.setEditable(false);

            changingUsername = false;
            changes.remove(changes.size() - 1);
        }
    }//GEN-LAST:event_changeUsernameButtonActionPerformed

