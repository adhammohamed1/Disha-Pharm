    /**
     * Search for deliverers related to the input entered in the search bar and display the
     * results in the deliverer table.
     * @author Adham Mohamed Aly
     */
    private void getDelivererSearchResults() {
        int inputID = 0;
        ArrayList<Deliverer> results;
        if (delivererSearchBar.getText().trim().isEmpty() || delivererSearchBar.getForeground() == Color.GRAY) {
            delivererSearchProgress.setValue(0);
            label1.setText("Found: 0 results");
            Table.loadDelivererTable(delivererTable, Deliverer.deliverers);
        } else {
            try {
                inputID = Integer.parseInt(delivererSearchBar.getText().trim());
                results = new Searcher().getDelivererSearchResults(inputID, delivererSearchProgress, label1);
            } catch (NumberFormatException e) {
                results = new Searcher().getDelivererSearchResults(delivererSearchBar.getText().trim(), delivererSearchProgress, label1);
            }
            if (results != null) {
                Table.loadDelivererTable(delivererTable, results);
            }
        }
    }

    /**
     * Search for pharmacists related to the input entered in the search bar and display the
     * results in the pharmacists table.
     * @author Adham Mohamed Aly
     */
    private synchronized void getPharmacistSearchResults() {
        int inputID = 0;
        ArrayList<Pharmacist> results;

        if (pharmacistSearchBar.getText().trim().isEmpty() || pharmacistSearchBar.getForeground() == Color.GRAY) {
            pharmacistSearchProgress.setValue(0);
            label.setText("Found: 0 results");
            Table.loadPharmacistTable(pharmacistTable, Pharmacist.pharmacists);
        } else {
            try {
                inputID = Integer.parseInt(pharmacistSearchBar.getText().trim());
                results = new Searcher().getPharmacistSearchResults(inputID, pharmacistSearchProgress, label);
            } catch (NumberFormatException e) {
                results = new Searcher().getPharmacistSearchResults(pharmacistSearchBar.getText().trim(), pharmacistSearchProgress, label);
            }
            if (results != null) {
                Table.loadPharmacistTable(pharmacistTable, results);
            }
        }
    }

