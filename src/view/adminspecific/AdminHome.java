    /**
     * Updates the completed operations lists in the 'Financial' tab
     * @author Adham Mohamed Aly
     */
    private void updateOpList() {
        DefaultListModel<Operation> listModel = new DefaultListModel<>();
        for (int opID : Operation.completedOperations.keySet()) {
            listModel.addElement(Operation.completedOperations.get(opID));
        }
        opList.setModel(listModel);
    }

    /**
     * Set the value of the 'Month Net Balance' label
     * @author Adham Mohamed Aly
     */
    private void setBalanceLabel() {
        Balance.adjustMonthBalance();
        if (Balance.monthNetBalance > 0) {
            monthNetProfitLabel.setForeground(new java.awt.Color(0, 125, 0));
            monthNetProfitLabel.setText("+" + FinancialFactors.PREFERRED_CURRENCY + String.format("%.2f", Balance.monthNetBalance));
        } else if (Balance.monthNetBalance < 0) {
            monthNetProfitLabel.setForeground(new java.awt.Color(190, 0, 0));
            monthNetProfitLabel.setText("-" + FinancialFactors.PREFERRED_CURRENCY + String.format("%.2f", Math.abs(Balance.monthNetBalance)));
        } else {
            monthNetProfitLabel.setForeground(new java.awt.Color(120, 120, 120));
            monthNetProfitLabel.setText("+" + FinancialFactors.PREFERRED_CURRENCY + "0.00");
        }
    }

