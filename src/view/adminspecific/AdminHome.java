    private void viewInventoryButtonMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewInventoryButtonMouseEntered
        int count = 0;
        for (int id : Product.productInventory.keySet()) {
            if (Product.productInventory.get(id).getQuantityInStock() == 0) {
                count++;
            }
        }
        if ((count == 1)) {
            ToastMessage toast = new ToastMessage("Warning: 1 registered item is out of stock.",
                    true, getX() + 55, getY() + getHeight() - 55);

            toast.showToast();
        } else if (count > 0) {
            ToastMessage toast = new ToastMessage("Warning: " + count + " registered items are out of stock.",
                    true, getX() + 55, getY() + getHeight() - 55);

            toast.showToast();
        }
    }//GEN-LAST:event_viewInventoryButtonMouseEntered

